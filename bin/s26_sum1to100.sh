#!/bin/bash

# 对 100 以内的所有正整数相加求和(1+2+3+4...+100)

# 对 100 以内的所有正整数相加求和(1+2+3+4...+100)

# seq 100 可以快速自动生成 100 个整数

sum=0
for i in `seq 100`
do
	sum=$[sum+i]
done

echo "sum:$sum"
